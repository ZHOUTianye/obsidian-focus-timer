let cachedLanguage = null;

function getLanguage() {
  if (cachedLanguage !== null) return cachedLanguage;
  const loc = obsidian.getLanguage().toLowerCase();
  cachedLanguage = loc.startsWith("zh") ? "zh" : loc;
  return cachedLanguage;
}

function t(key, lang = null) {
  const currentLang = lang || getLanguage();
  return translations[currentLang]?.[key] || translations.en[key] || key;
}

function getCurrentLanguage() {
  return getLanguage();
}

function resetLanguageCache() {
  cachedLanguage = null;
}

module.exports = { getLanguage, t, getCurrentLanguage, resetLanguageCache };
